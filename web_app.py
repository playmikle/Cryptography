def is_prime(n):
    if n < 2:
        return False
    if n == 2:
        return True
    if n % 2 == 0:
        return False
    for i in range(3, int(n**0.5) + 1, 2):
        if n % i == 0:
            return False
    return True

def gcd(a, b):
    while b:
        a, b = b, a % b
    return a

def mod_inverse(e, phi):
    for d in range(2, phi):
        if (e * d) % phi == 1:
            return d
    return None

def factorize_with_steps(n, callback):
    factors = []
    temp_n = n
    i = 2
    while i * i <= temp_n:
        if temp_n % i == 0:
            factors.append(i)
            temp_n //= i
        else:
            i += 1 if i == 2 else 2
    if temp_n > 1:
        factors.append(temp_n)
    return factors

def generate_rsa_keys(p, q):
    if not (is_prime(p) and is_prime(q)):
        return None, "–û–±–∞ —á–∏—Å–ª–∞ –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å –ø—Ä–æ—Å—Ç—ã–º–∏"
    if p == q:
        return None, "–ß–∏—Å–ª–∞ –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å —Ä–∞–∑–Ω—ã–º–∏"
    
    n = p * q
    phi = (p - 1) * (q - 1)
    
    e = 65537
    if e >= phi:
        e = 17
        while e < phi:
            if gcd(e, phi) == 1:
                break
            e += 2
    
    d = mod_inverse(e, phi)
    if d is None:
        return None, "–ù–µ —É–¥–∞–ª–æ—Å—å –Ω–∞–π—Ç–∏ –∑–∞–∫—Ä—ã—Ç—ã–π –∫–ª—é—á"
    
    return (n, e, d), "–£—Å–ø–µ—à–Ω–æ"

